import Foundation

/// Layout element text format type (matches Java FormatType enum)
public enum LayoutFormatType: Int {
    case normal = 0
    case bold = 1
    case italic = 2
    case boldItalic = 3
    case small = 4
    case smallBold = 5
    case smallItalic = 6
    case smallBoldItalic = 7
}
